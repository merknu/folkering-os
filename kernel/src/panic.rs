/// Draw a single character at (x, y) on the framebuffer
unsafe fn draw_char(fb: *mut u32, pitch_px: usize, x: usize, y: usize, ch: u8, color: u32) {
    if ch < 0x20 || ch > 0x7E {
        return;
    }
    let glyph = &FONT_8X8[(ch - 0x20) as usize];
    for row in 0..8 {
        let bits = glyph[row];
        for col in 0..8 {
            if bits & (0x80 >> col) != 0 {
                let px = x + col;
                let py = y + row;
                *fb.add(py * pitch_px + px) = color;
            }
        }
    }
}

/// Draw a string at (x, y), returns number of characters drawn
unsafe fn draw_string(fb: *mut u32, pitch_px: usize, x: usize, y: usize, s: &str, color: u32) -> usize {
    let mut cx = x;
    let mut count = 0;
    for &byte in s.as_bytes() {
        if byte == b'\n' {
            break;
        }
        draw_char(fb, pitch_px, cx, y, byte, color);
        cx += 8;
        count += 1;
    }
    count
}

/// Draw a hex value (e.g., "0xDEADBEEF")
unsafe fn draw_hex(fb: *mut u32, pitch_px: usize, x: usize, y: usize, val: u64, color: u32) -> usize {
    let mut buf = [0u8; 18]; // "0x" + 16 hex digits
    buf[0] = b'0';
    buf[1] = b'x';
    let hex_chars = b"0123456789ABCDEF";
    for i in 0..16 {
        let nibble = ((val >> (60 - i * 4)) & 0xF) as usize;
        buf[2 + i] = hex_chars[nibble];
    }
    let s = core::str::from_utf8_unchecked(&buf);
    draw_string(fb, pitch_px, x, y, s, color)
}

/// Fill a rectangle with a solid color
unsafe fn fill_rect(fb: *mut u32, pitch_px: usize, x: usize, y: usize, w: usize, h: usize, color: u32) {
    for row in y..y + h {
        for col in x..x + w {
            *fb.add(row * pitch_px + col) = color;
        }
    }
}

/// Read a CPU register by reading inline assembly values
fn read_registers() -> (u64, u64, u64) {
    let rsp: u64;
    let rbp: u64;
    let rflags: u64;
    unsafe {
        core::arch::asm!("mov {}, rsp", out(reg) rsp);
        core::arch::asm!("mov {}, rbp", out(reg) rbp);
        core::arch::asm!("pushfq; pop {}", out(reg) rflags);
    }
    (rsp, rbp, rflags)
}

/// Panic handler for kernel panics
///
/// Displays a graphical panic screen on the framebuffer AND serial output.
#[panic_handler]
fn panic(info: &PanicInfo) -> ! {
    use x86_64::instructions::interrupts;

    // Disable interrupts immediately
    interrupts::disable();

    // Capture registers before we clobber them
    let (rsp, rbp, rflags) = read_registers();
    let debug_marker = crate::arch::x86_64::syscall::DEBUG_MARKER
        .load(core::sync::atomic::Ordering::Relaxed);

    // ── Serial output (always works) ──
    crate::drivers::serial::_print(format_args!("\n\n"));
    crate::drivers::serial::_print(format_args!("╔════════════════════════════════════════╗\n"));
    crate::drivers::serial::_print(format_args!("║         KERNEL PANIC                   ║\n"));
    crate::drivers::serial::_print(format_args!("╚════════════════════════════════════════╝\n\n"));

    if let Some(location) = info.location() {
        crate::drivers::serial::_print(format_args!(
            "Location: {}:{}:{}\n",
            location.file(), location.line(), location.column()
        ));
    }

    let message = info.message();
    crate::drivers::serial::_print(format_args!("Message: {}\n", message));
    crate::drivers::serial::_print(format_args!("RSP: {:#018x}\n", rsp));
    crate::drivers::serial::_print(format_args!("RBP: {:#018x}\n", rbp));
    crate::drivers::serial::_print(format_args!("RFLAGS: {:#018x}\n", rflags));
    crate::drivers::serial::_print(format_args!("DEBUG_MARKER: {:#018x}\n", debug_marker));
    crate::drivers::serial::_print(format_args!("\nSystem halted.\n"));

    // ── Graphical panic screen ──
    // Try to get framebuffer info from boot_info
    let bi = crate::boot_info::get_boot_info();
    let fb_phys = bi.framebuffer.physical_address;
    let fb_width = bi.framebuffer.width as usize;
    let fb_height = bi.framebuffer.height as usize;
    let fb_pitch = bi.framebuffer.pitch as usize;

    if fb_phys != 0 && fb_width > 0 && fb_height > 0 {
        let fb_virt = crate::phys_to_virt(fb_phys as usize);
        let fb = fb_virt as *mut u32;
        let pitch_px = fb_pitch / 4; // bytes to pixels (32bpp)

        // Colors
        let bg: u32      = 0x001A1A2E;  // Dark navy
        let red: u32     = 0x00FF4444;  // Panic red
        let white: u32   = 0x00E0E0E0;  // Light gray
        let yellow: u32  = 0x00FFD700;  // Gold
        let dim: u32     = 0x00888888;  // Dim gray
        let accent: u32  = 0x0000CED1;  // Teal

        unsafe {
            // Fill entire screen with dark background
            fill_rect(fb, pitch_px, 0, 0, fb_width, fb_height, bg);

            // Red banner at top
            fill_rect(fb, pitch_px, 0, 0, fb_width, 48, 0x00CC0000);

            // Title
            let title = "FOLKERING OS - KERNEL PANIC";
            let tx = (fb_width - title.len() * 8) / 2;
            draw_string(fb, pitch_px, tx, 16, title, white);

            // Content area
            let mut y = 72;
            let x = 40;

            // Panic message
            draw_string(fb, pitch_px, x, y, "REASON:", yellow);
            y += 16;

            // Format message to a fixed buffer
            let msg = info.message();
            let mut msg_buf = [0u8; 120];
            let mut msg_len = 0;
            use core::fmt::Write;
            struct BufWriter<'a>(&'a mut [u8], usize);
            impl<'a> core::fmt::Write for BufWriter<'a> {
                fn write_str(&mut self, s: &str) -> core::fmt::Result {
                    for &b in s.as_bytes() {
                        if self.1 < self.0.len() {
                            self.0[self.1] = b;
                            self.1 += 1;
                        }
                    }
                    Ok(())
                }
            }
            let mut writer = BufWriter(&mut msg_buf, 0);
            let _ = write!(writer, "{}", msg);
            msg_len = writer.1;

            if let Ok(s) = core::str::from_utf8(&msg_buf[..msg_len]) {
                // Split into lines of ~80 chars
                let max_chars = (fb_width - 80) / 8;
                let mut start = 0;
                while start < s.len() {
                    let end = (start + max_chars).min(s.len());
                    draw_string(fb, pitch_px, x + 16, y, &s[start..end], white);
                    y += 12;
                    start = end;
                }
            }
            y += 8;

            // Location
            if let Some(loc) = info.location() {
                draw_string(fb, pitch_px, x, y, "LOCATION:", yellow);
                y += 16;

                let mut loc_buf = [0u8; 100];
                let mut lw = BufWriter(&mut loc_buf, 0);
                let _ = write!(lw, "{}:{}:{}", loc.file(), loc.line(), loc.column());
                let loc_len = lw.1;
                if let Ok(s) = core::str::from_utf8(&loc_buf[..loc_len]) {
                    draw_string(fb, pitch_px, x + 16, y, s, white);
                }
                y += 20;
            }

            // Registers
            draw_string(fb, pitch_px, x, y, "REGISTERS:", yellow);
            y += 16;

            draw_string(fb, pitch_px, x + 16, y, "RSP    = ", dim);
            draw_hex(fb, pitch_px, x + 16 + 9 * 8, y, rsp, accent);
            y += 12;

            draw_string(fb, pitch_px, x + 16, y, "RBP    = ", dim);
            draw_hex(fb, pitch_px, x + 16 + 9 * 8, y, rbp, accent);
            y += 12;

            draw_string(fb, pitch_px, x + 16, y, "RFLAGS = ", dim);
            draw_hex(fb, pitch_px, x + 16 + 9 * 8, y, rflags, accent);
            y += 12;

            draw_string(fb, pitch_px, x + 16, y, "DEBUG  = ", dim);
            draw_hex(fb, pitch_px, x + 16 + 9 * 8, y, debug_marker, accent);
            y += 24;

            // Footer
            let footer = "System halted. Reboot to continue.";
            let fx = (fb_width - footer.len() * 8) / 2;
            draw_string(fb, pitch_px, fx, fb_height - 40, footer, dim);
        }
    }

    // Halt CPU
    loop {
        x86_64::instructions::hlt();
    }
}
